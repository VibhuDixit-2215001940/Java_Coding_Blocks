public class RelationalOperator {
    public static void main(String[] args) {
        // Relational Operators are == , !=  , > , < , >= , <= 
        //Note assignment operator =
        //Equal to equal to operator == returns boolean value
        int a = 10,b=10;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}
