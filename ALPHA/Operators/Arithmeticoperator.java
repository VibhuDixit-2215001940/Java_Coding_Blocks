public class Arithmeticoperator {
    public static void main(String[] args) {
        // 1.BINARY (+,-,*,/,%(modulo remainder)) needs two operands i.e. a & b 
        // 2.UNARY (++,--)
        int a = 10;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
