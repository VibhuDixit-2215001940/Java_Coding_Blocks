public class Data_Types {
    public static void main(String[] args) {
        //There are two types of datatypes.
        //A.PRIMITIVE DT:- Which are predifines in java ex.byte,short,char,float,bool,int,double,long
        //B.NON PRIMITIVE DT:- String,Array,Class,Object,Interface

        //Java is a typed language as we have to give the data type.
        int a = 10;
        System.out.println(a);
        byte b = 0;
        System.out.println(b);
        char c = 'a';
        System.out.println(c);
        boolean t = true;
        System.out.println(t);
        float d = 10.5f;
        System.out.println(d);
        short n = 240;
        System.out.println(n);
    }
}
